package com.jsonde.client.sun;

public class VirtualMachineServiceException extends Exception {

    public VirtualMachineServiceException() {
    }

    public VirtualMachineServiceException(String message) {
        super(message);
    }

    public VirtualMachineServiceException(String message, Throwable cause) {
        super(message, cause);
    }

    public VirtualMachineServiceException(Throwable cause) {
        super(cause);
    }

}
